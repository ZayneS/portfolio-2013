									<h3>YOGURT is</h3>
									<p>a Google Chrome extension providing awareness of online distractions. It visualizes your online browsing history aiming to raise awareness of online distraction. It was the final project developed in 
										<a href="https://graphics.stanford.edu/wikis/cs448b-12-fall/" target="_blank">CS448B Data Visualization</a>
									class at Stanford. It was done with three awesome partners: Borui Wang, Jianfeng Hu, and Ningxia Zhang. I did most of the design of visualization and interaction, and part of the front-end coding.</p>
									<p>You can find 
										<a href="https://chrome.google.com/webstore/detail/yogurt/bkcllpfdmadccnllfpkeipobfhclbjnf" target="_blank">Yogurt in Chrome Web Store</a>.
									</p>
									<p>We also developed a data visualization technique called "barcode chart" during the development of Yogurt. The full paper about "barcode chart" can be found 
										<a href="/files/yogurt.pdf" target="_blank">here</a>
									.</p>
									<h3>NEEDFINDING...</h3>
									<p>Nowadays, our life pattern has been dramatically changed when internet has become the new habitat for human. But have we become more efficient or more distracted? What if we visualize people's browser history, and let them be aware, and even be surprised by their own online behavior? It would be a lot of fun. And we believe it will be a very friendly way to help people manage distraction by simply keep them aware of that.</p>
									<h3>INSPIRATION...</h3>
									<p>DNA art is an artistic piece from a person's own DNA. It is a mystery, highly-encrypted self-portrait of the person's genetic makeup, identity, personality and even behavior.</p>
									<p>Yogurt is inspired by DNA art. Since one's online behavior is a part of himself, which is also unique and personal, It is very interesting to display his online behavior in this similar format. Yogurt can bring user the awareness of online distraction in a modest, friendly way via artistic appearance, and keep the information with the feeling of mystery for personal privacy.</p>
