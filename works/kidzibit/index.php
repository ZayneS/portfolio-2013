									<h3>INSIGHTS...</h3>
									<p>- Kids capture a memory by documenting a moment in time or an experience in some way. The documentation helps them remember their moments with loved ones, especially when they look back at it at different points in time.  Josiah, for example, makes pieces of art that represent his experiences (going to Seaworld, the mountains, etc) and reminds him of them, and he talks about his experiences creating and interacting with art in great detail.</p>
									<p>- Having an interactive experience of creation allows kids to take ownership of the experience through the unique object they’ve made - Josiah focused on his independence in creating, and how his creating and creations were distinct from those of his siblings.</p>
									<p>- Kids prefer sculptures and more tangible forms of art to paintings and images.</p>
									<p>- When looking at photographs, kids only engaged with photos when the photos had kids in it.
									</p>
